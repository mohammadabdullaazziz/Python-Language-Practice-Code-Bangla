# স্লাইসিং (অংশবিশেষ)

# মূল স্ট্রিং ভেরিয়েবল
text = "Hello Python"

# স্লাইসিং করে নির্দিষ্ট অংশ আলাদা করা
substring_hello = text[0:5]  # ০ থেকে শুরু করে ৫ এর ঠিক আগের ঘর (৪) পর্যন্ত
substring_python = text[6:]  # ৬ নম্বর থেকে শুরু করে একদম শেষ পর্যন্ত
reversed_text = text[::-1]    # পুরো টেক্সটটিকে একদম উল্টো করে ফেলা

#  f-string ব্যবহার করে সুন্দর করে আউটপুট প্রিন্ট 
print(f"Original Text: '{text}'")
print(f"Sliced Part 1 [0:5]: '{substring_hello}'")
print(f"Sliced Part 2 [6:]: '{substring_python}'")
print(f"Reversed Text [::-1]: '{reversed_text}'")




